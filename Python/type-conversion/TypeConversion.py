# Inteiro para String
intType = 123
intToString = str(intType)
print(intToString)
# String para Inteiro
stringToInt = int(intToString)
print(stringToInt)

# Double para String
doubleType = 123.12
doubleToString = str(doubleType)
print(doubleToString)
# String para double
stringToDouble = float(doubleToString)
print(stringToDouble)

# Boleano para Inteiro
booleanType = True
booleanToInt = int(booleanType)
print(booleanToInt)
