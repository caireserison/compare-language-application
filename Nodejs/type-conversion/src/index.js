// Inteiro para String
intType = 123
intToString = intType.toString()
console.log(intToString)
// String para Inteiro
stringToInt = parseInt(intToString)
console.log(stringToInt)

// Double para String
doubleType = 123.12
doubleToString = doubleType.toString()
console.log(doubleToString)
// String para double
stringToDouble = parseFloat(doubleToString)
console.log(stringToDouble)

// Boleano para Inteiro
booleanType = true
booleanToInt = booleanType ? 1 : 0
console.log(booleanToInt)
